"""Listas en python"""


"""Listas: pop(): Quita un elemento de una posicion dada"""

paises = ["Perú", "Canada", "Argentina", "Mexico", "Epaña"]

print("Mi lista inicial: {}".format(paises))

paises.pop()

print("Mi lista actualizada es: {}".format(paises))

print("paises[2]: {}".format(paises[2]))

paises.pop(2)
print("La lista actualizada es: {}".format(paises))

paises.pop(2)
print("La lista actualizada es: {}".format(paises))


